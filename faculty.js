function hideEverything(){
    $("#lopetegui").hide();
    $("#zidane").hide();
    $("#benitez").hide();
    $("#ancelotti").hide();
    $("#mourinho").hide();
}

function lopetegui(){
    $("#lopetegui").show();
    $("#zidane").hide();
    $("#benitez").hide();
    $("#ancelotti").hide();
    $("#mourinho").hide();
}

function zidane(){
    $("#lopetegui").hide();
    $("#zidane").show();
    $("#benitez").hide();
    $("#ancelotti").hide();
    $("#mourinho").hide();
}

function benitez(){
    $("#lopetegui").hide();
    $("#zidane").hide();
    $("#benitez").show();
    $("#ancelotti").hide();
    $("#mourinho").hide();
}

function ancelotti(){
    $("#lopetegui").hide();
    $("#zidane").hide();
    $("#benitez").hide();
    $("#ancelotti").show();
    $("#mourinho").hide();
}

function mourinho(){
    $("#lopetegui").hide();
    $("#zidane").hide();
    $("#benitez").hide();
    $("#ancelotti").hide();
    $("#mourinho").show();
}


